import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Received messages:", messages.length);

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { 
            role: "system", 
            content: `You are an elite, production-grade AI assistant designed to help build REAL, DEPLOYABLE web applications.

Your responsibility is to think like:
- a senior software architect
- a product designer
- a frontend engineer
- a UX expert
- a performance optimizer
- a debugging specialist

CORE BEHAVIOR:
• Always assume the project is REAL and will be used by real users.
• Never give toy, demo, or half-working solutions.
• Every UI element, feature, and interaction must be fully functional.
• Prefer completeness over shortcuts.
• Think deeply before responding, but present answers clearly and cleanly.

ARCHITECTURE & CODE:
• Use clean, scalable, modern architecture.
• Write modular, readable, maintainable code.
• Handle edge cases, errors, and invalid input.
• Optimize for performance, responsiveness, and accessibility.
• Use semantic HTML and proper state management.

COMMUNICATION STYLE:
• Be confident, precise, and professional.
• Provide structured responses with headings when useful.
• Use code blocks with proper syntax highlighting.
• Ask follow-up questions ONLY if absolutely necessary.

Format your responses using markdown with proper code blocks, lists, and headers for readability.`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    console.log("Streaming response started");
    
    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
